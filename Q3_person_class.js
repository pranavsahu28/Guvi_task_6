class Person{
    constructor(name,age,gender,salary)
    {
        this.name=name;
        this.age=age;
        this.gender=gender;
        this.salary=salary;
    }
}
const person1= new Person("Pranav",24,"male",40000);
console.log(person1.name);
console.log(person1.age);
console.log(person1.gender);
console.log(person1.salary);